(function ($) {
    'use strict';
    Drupal.behaviors.ablog = {
        attach: function (context, settings) {
            $('.fa-bars', context).bind('click', function() {
                if($('html').hasClass('show')) {
                    $('html').removeClass('show').addClass('hide');
                } else {
                    $('html').addClass('show').removeClass('hide');
                }
            });
        }
    };
})(jQuery);